def delete_user_data(telegram_id: int) -> list:
    """Удаляет все данные пользователя. Возвращает список ID удалённых лекарств."""
    with get_connection() as conn:
        user = conn.execute(
            "SELECT id FROM users WHERE telegram_id = %s", (telegram_id,)
        ).fetchone()
        if not user:
            return []
        user_id = user["id"]
        med_ids = [r["id"] for r in conn.execute(
            "SELECT id FROM medications WHERE user_id = %s", (user_id,)
        ).fetchall()]
        if med_ids:
            conn.execute("DELETE FROM intake_log WHERE medication_id = ANY(%s)", (med_ids,))
            conn.execute("DELETE FROM schedule_rules WHERE medication_id = ANY(%s)", (med_ids,))
            conn.execute("DELETE FROM medications WHERE user_id = %s", (user_id,))
        conn.execute("DELETE FROM dependents WHERE user_id = %s", (user_id,))
        conn.execute("DELETE FROM users WHERE telegram_id = %s", (telegram_id,))
        return med_ids


