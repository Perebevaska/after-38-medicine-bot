def delete_dependent(telegram_id: int, dependent_id: int) -> list:
    """Удаляет подопечного и деактивирует его лекарства. Возвращает список ID лекарств."""
    with get_connection() as conn:
        user = conn.execute(
            "SELECT id FROM users WHERE telegram_id = %s", (telegram_id,)
        ).fetchone()
        if not user:
            return []
        user_id = user["id"]
        dep = conn.execute(
            "SELECT id FROM dependents WHERE id = %s AND user_id = %s", (dependent_id, user_id)
        ).fetchone()
        if not dep:
            return []
        med_ids = [r["id"] for r in conn.execute(
            "SELECT id FROM medications WHERE user_id = %s AND dependent_id = %s",
            (user_id, dependent_id)
        ).fetchall()]
        if med_ids:
            conn.execute("DELETE FROM intake_log WHERE medication_id = ANY(%s)", (med_ids,))
            conn.execute("DELETE FROM schedule_rules WHERE medication_id = ANY(%s)", (med_ids,))
            # dependent_id = NULL обязательно: иначе DELETE подопечного нарушит FK
            conn.execute(
                "UPDATE medications SET active = 0, dependent_id = NULL WHERE dependent_id = %s AND user_id = %s",
                (dependent_id, user_id)
            )
        conn.execute("DELETE FROM dependents WHERE id = %s", (dependent_id,))
        return med_ids


