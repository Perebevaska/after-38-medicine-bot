_CODE_CHARS = "23456789ABCDEFGHJKMNPQRSTUVWXYZ"


def _gen_caregiver_code() -> str:
    c = "".join(secrets.choice(_CODE_CHARS) for _ in range(8))
    return f"{c[:4]}-{c[4:]}"


def init_pool(dsn: str | None = None):
    """Инициализирует пул соединений. Вызывать один раз до init_db()."""
    global _pool
    if _pool is not None:
        return
    dsn = dsn or os.environ["DATABASE_URL"]
    _pool = ConnectionPool(dsn, min_size=1, max_size=10)


def close_pool():
    global _pool
    if _pool:
        _pool.close()
        _pool = None


@contextmanager
def get_connection():
    """Контекстный менеджер для подключения к БД."""
    if _pool is None:
        init_pool()
    conn = _pool.getconn()
    conn.row_factory = dict_row
    try:
        yield conn
        conn.commit()
    except psycopg.Error as e:
        conn.rollback()
        db_logger.error("Ошибка БД: %s", e)
        raise DatabaseError("Ошибка при работе с базой данных") from e
    except Exception:
        conn.rollback()
        raise
    finally:
        _pool.putconn(conn)


