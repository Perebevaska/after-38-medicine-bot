def _gen_dep_share_code() -> str:
    c = "".join(secrets.choice(_CODE_CHARS) for _ in range(12))
    return f"{c[:4]}-{c[4:8]}-{c[8:]}"


def ensure_dep_share_code(dep_id: int, owner_telegram_id: int) -> str:
    """Возвращает share_code для близкого, генерирует при первом вызове. Проверяет владельца."""
    with get_connection() as conn:
        dep = conn.execute(
            "SELECT d.id, d.share_code FROM dependents d "
            "JOIN users u ON u.id = d.user_id "
            "WHERE d.id = %s AND u.telegram_id = %s",
            (dep_id, owner_telegram_id)
        ).fetchone()
        if not dep:
            raise DatabaseError("Близкий не найден")
        if dep["share_code"]:
            return dep["share_code"]
        for _ in range(10):
            code = _gen_dep_share_code()
            updated = conn.execute(
                "UPDATE dependents SET share_code = %s "
                "WHERE id = %s AND share_code IS NULL RETURNING share_code",
                (code, dep_id)
            ).fetchone()
            if updated:
                return updated["share_code"]
            row = conn.execute("SELECT share_code FROM dependents WHERE id = %s", (dep_id,)).fetchone()
            if row and row["share_code"]:
                return row["share_code"]
        raise DatabaseError("Не удалось сгенерировать уникальный код")


def request_dep_share(viewer_telegram_id: int, code: str) -> dict:
    """Создаёт pending-запрос на наблюдение. Возвращает {share_id, dep_name, owner_telegram_id}."""
    with get_connection() as conn:
        dep = conn.execute(
            "SELECT d.id, d.name, u.id AS owner_user_id, u.telegram_id AS owner_telegram_id "
            "FROM dependents d JOIN users u ON u.id = d.user_id "
            "WHERE d.share_code = %s",
            (code,)
        ).fetchone()
        if not dep:
            raise DatabaseError("Код не найден")
        viewer = conn.execute(
            "SELECT id, username FROM users WHERE telegram_id = %s", (viewer_telegram_id,)
        ).fetchone()
        if not viewer:
            raise DatabaseError("Пользователь не найден")
        if viewer["id"] == dep["owner_user_id"]:
            raise DatabaseError("Нельзя наблюдать за собственным близким")
        existing = conn.execute(
            "SELECT status FROM dependent_shares WHERE dep_id = %s AND viewer_user_id = %s",
            (dep["id"], viewer["id"])
        ).fetchone()
        if existing and existing["status"] == "active":
            raise DatabaseError("Уже наблюдаете за этим близким")
        active_count = conn.execute(
            "SELECT COUNT(*) AS n FROM dependent_shares WHERE dep_id = %s AND status = 'active'",
            (dep["id"],)
        ).fetchone()["n"]
        if active_count >= 1:
            raise DatabaseError("Достигнут лимит наблюдателей (1)")
        share = conn.execute(
            """INSERT INTO dependent_shares (dep_id, owner_user_id, viewer_user_id, status)
               VALUES (%s, %s, %s, 'pending')
               ON CONFLICT (dep_id, viewer_user_id)
               DO UPDATE SET status = 'pending'
               RETURNING id""",
            (dep["id"], dep["owner_user_id"], viewer["id"])
        ).fetchone()
        return {
            "share_id": share["id"],
            "dep_name": dep["name"],
            "owner_telegram_id": dep["owner_telegram_id"],
            "viewer_username": viewer["username"],
        }


def confirm_dep_share(share_id: int, owner_telegram_id: int) -> dict:
    """Подтверждает запрос. Возвращает {viewer_telegram_id, dep_name}."""
    with get_connection() as conn:
        owner = conn.execute(
            "SELECT id FROM users WHERE telegram_id = %s", (owner_telegram_id,)
        ).fetchone()
        if not owner:
            raise DatabaseError("Пользователь не найден")
        active_count = conn.execute(
            """SELECT COUNT(*) AS n FROM dependent_shares ds
               JOIN dependent_shares s2 ON s2.dep_id = ds.dep_id
               WHERE ds.id = %s AND ds.owner_user_id = %s AND s2.status = 'active'""",
            (share_id, owner["id"])
        ).fetchone()["n"]
        if active_count >= 1:
            raise DatabaseError("Достигнут лимит наблюдателей (1)")
        row = conn.execute(
            """UPDATE dependent_shares SET status = 'active'
               WHERE id = %s AND owner_user_id = %s AND status = 'pending'
               RETURNING dep_id, viewer_user_id""",
            (share_id, owner["id"])
        ).fetchone()
        if not row:
            raise DatabaseError("Запрос не найден")
        viewer = conn.execute(
            "SELECT telegram_id FROM users WHERE id = %s", (row["viewer_user_id"],)
        ).fetchone()
        dep = conn.execute("SELECT name FROM dependents WHERE id = %s", (row["dep_id"],)).fetchone()
        return {
            "viewer_telegram_id": viewer["telegram_id"] if viewer else None,
            "dep_name": dep["name"] if dep else "",
        }


