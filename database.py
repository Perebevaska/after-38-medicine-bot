def migrate():
    """Добавляет новые колонки если их нет (миграция). Идемпотентна через IF NOT EXISTS."""
    with get_connection() as conn:
        for col, definition in [
            ("timezone",           "TEXT DEFAULT 'UTC'"),
            ("reminder_mode",      "TEXT DEFAULT 'once'"),
            ("time_morning",       "TEXT DEFAULT '09:00'"),
            ("time_lunch",         "TEXT DEFAULT '12:00'"),
            ("time_evening",       "TEXT DEFAULT '18:00'"),
            ("time_night",         "TEXT DEFAULT '22:00'"),
            ("daily_plan_enabled", "INTEGER DEFAULT 1"),
            ("daily_plan_time",    "TEXT DEFAULT '08:00'"),
            ("caregiver_enabled",  "INTEGER DEFAULT 0"),
        ]:
            conn.execute(
                f"ALTER TABLE IF EXISTS users ADD COLUMN IF NOT EXISTS {col} {definition}"
            )

        conn.execute(
            "ALTER TABLE IF EXISTS schedule_rules ADD COLUMN IF NOT EXISTS dosage TEXT DEFAULT NULL"
        )

        for col, definition in [
            ("dependent_id",   "INTEGER DEFAULT NULL REFERENCES dependents(id)"),
            ("stock_qty",      "REAL DEFAULT NULL"),
            ("units_per_dose", "REAL DEFAULT 1"),
            ("low_stock_days", "INTEGER DEFAULT 5"),
            ("paused",         "INTEGER DEFAULT 0"),
        ]:
            conn.execute(
                f"ALTER TABLE IF EXISTS medications ADD COLUMN IF NOT EXISTS {col} {definition}"
            )

        conn.execute("DROP TABLE IF EXISTS schedules")


